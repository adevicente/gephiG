    private void botonImportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonImportarActionPerformed
        // TODO add your handling code here:
        if (red != null) {
            grafo = new GephiG(red);
            JOptionPane.showMessageDialog(null, "Red importada con éxito");
        } else {
            JOptionPane.showMessageDialog(null, "No se ha importado ninguna red");
        }
    }//GEN-LAST:event_botonImportarActionPerformed

    private void botonResaltarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonResaltarActionPerformed
        // TODO add your handling code here:
        if (grafo != null) {
            if (grafo.resaltar(campoGen.getText(), grafo.getGraphmodel())) {
                JOptionPane.showMessageDialog(null, "Nodo resaltado junto a sus adyacentes");
            } else {
                JOptionPane.showMessageDialog(null, "No existe el nodo");
            }
            
        } else {
            JOptionPane.showMessageDialog(null, "No se ha importado ninguna red");
        }
    }//GEN-LAST:event_botonResaltarActionPerformed

    private void botonExportarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonExportarActionPerformed
        // TODO add your handling code here:
        if (grafo != null) {
            //Aplicamos layout
            if (rButtonYifanHu.isSelected()) {
                grafo.aplicarYifanHu();
            }

            //Elegimos la extension deseada
            if (rButtonPDF.isSelected()) {
                if (grafo.exportar("pdf")) {
                    JOptionPane.showMessageDialog(null, "Red exportada con éxito");
                }
            } else if (rButtonPNG.isSelected()) {
                if (grafo.exportar("png")) {
                    JOptionPane.showMessageDialog(null, "Red exportada con éxito");
                }
            } else if (rButtonGML.isSelected()) {
                if (grafo.exportar("gml")) {
                    JOptionPane.showMessageDialog(null, "Red exportada con éxito");
                }
            } else if (rButtonPajek.isSelected()) {
                if (grafo.exportar("pajek")) {
                    JOptionPane.showMessageDialog(null, "Red exportada con éxito");
                }
            } else if (rButtonCSV.isSelected()) {
                if (grafo.exportar("csv")) {
                    JOptionPane.showMessageDialog(null, "Red exportada con éxito");
                }
            }

            BufferedImage myPicture;
            try {
                myPicture = ImageIO.read(new File("red.png"));
                JLabel picLabel = new JLabel(new ImageIcon(myPicture));
                jPanel3.add(picLabel);
            } catch (IOException ex) {
                Exceptions.printStackTrace(ex);
            }

        } else {
            JOptionPane.showMessageDialog(null, "No se ha importado ninguna red");
        }
    }//GEN-LAST:event_botonExportarActionPerformed

