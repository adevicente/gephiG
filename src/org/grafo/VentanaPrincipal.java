    private void botonAbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAbrirActionPerformed
        // TODO add your handling code here:
        JFileChooser fc = new JFileChooser();
        int returnVal = fc.showOpenDialog(VentanaPrincipal.this);

        if (returnVal == JFileChooser.APPROVE_OPTION) {
            red = fc.getSelectedFile();
            //This is where a real application would open the file.
            fieldArchivo.setText(red.getName());
        } else {
            fieldArchivo.setText("¡Operacion cancelada!");
        }
        fieldArchivo.setCaretPosition(fieldArchivo.getDocument().getLength());
    }//GEN-LAST:event_botonAbrirActionPerformed

